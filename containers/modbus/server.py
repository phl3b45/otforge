def _sensor_value_at(t: float) -> float:
    """
    Computes the smart-sensor's raw engineering value at elapsed time t (seconds),
    before noise, per SENSOR_WAVEFORM. Mirrors the waveform descriptions in
    SensorConfig (packages/schema/src/icslab.ts):
      sine     -- smooth oscillation between min and max, period 60 s
      random   -- white noise bounded by min/max (ignores t)
      sawtooth -- linear ramp 0->1 every 60 s then reset
      square   -- two-state, half-period 30 s
      constant -- fixed at (min + max) / 2
    """
    lo, hi = SENSOR_MIN_VALUE, SENSOR_MAX_VALUE
    mid, span = (lo + hi) / 2.0, (hi - lo) / 2.0
    period = 60.0

    if SENSOR_WAVEFORM == "sine":
        return mid + span * math.sin(2 * math.pi * t / period)
    if SENSOR_WAVEFORM == "random":
        return random.uniform(lo, hi)
    if SENSOR_WAVEFORM == "sawtooth":
        return lo + (hi - lo) * ((t % period) / period)
    if SENSOR_WAVEFORM == "square":
        return hi if (t % period) < period / 2 else lo
    return mid  # constant


def build_store() -> ModbusSlaveContext:
    """
    Creates the pymodbus slave context with category-appropriate register values.

    All four data blocks are allocated (DI, CO, HR, IR). IR mirrors HR so both
    FC3 and FC4 reads return consistent values matching real device behaviour.
    smart-sensor allocates enough registers to cover SENSOR_MODBUS_REGISTER,
    since the author picks that address freely (not fixed at HR0 like every
    other category here).

    Initial non-zero values are applied via ctx.setValues() AFTER construction,
    not via the ModbusSequentialDataBlock(address, values) constructor's values
    list. pymodbus 3.7.4's constructor has an off-by-one: a value placed at Python
    list index N there is only reachable at client/getValues address N+1, while
    setValues(fc, N, ...) and client reads at address N agree with each other.
    This is invisible for rtu/sensor (DEFAULTS-seeded) because the periodic
    simulate() drift task overwrites the affected registers via setValues()
    within 2 s of startup, masking the constructor's shifted seed -- but
    wellhead-controller's HR0/HR1 are never overwritten, so it stayed visible
    forever. Confirmed empirically against the live rtu container before fixing.
    """
    block_size = 100
    if CATEGORY == "smart-sensor":
        block_size = max(block_size, SENSOR_MODBUS_REGISTER + 1)

    ctx = ModbusSlaveContext(
        di=ModbusSequentialDataBlock(0, [0] * block_size),
        co=ModbusSequentialDataBlock(0, [0] * block_size),
        hr=ModbusSequentialDataBlock(0, [0] * block_size),
        ir=ModbusSequentialDataBlock(0, [0] * block_size),
    )

    def seed(address: int, value: int) -> None:
        clamped = max(0, min(65535, value))
        ctx.setValues(3, address, [clamped])
        ctx.setValues(4, address, [clamped])

    for reg, val in enumerate(DEFAULTS.get(CATEGORY, [])):
        if val:
            seed(reg, val)
    if CATEGORY == "smart-sensor":
        seed(SENSOR_MODBUS_REGISTER, round(_sensor_value_at(0.0) * 10))
    if CATEGORY == "smart-controller" and CONTROLLER_KIND == "wellhead-controller":
        # Live read/write setpoints, not gated by CO0 -- every other kind starts
        # at 0 (stopped/closed) until a student writes CO0=1, so needs no seeding.
        seed(0, round(CONTROLLER_CHOKE_POSITION_PCT * 10))
        seed(1, round(CONTROLLER_DOWNHOLE_PRESSURE_SETPOINT_BAR * 10))

    return ctx


async def simulate(store: ModbusSlaveContext) -> None:
    """
    Background task: drifts process registers every 2 s to simulate live
    field measurements. Writes to both HR (FC3) and IR (FC4) to keep them
    in sync.
    """
    if CATEGORY not in DRIFT:
        return
    specs = DRIFT[CATEGORY]
    for reg, mag in specs:
        log.info("Simulation: HR%d drifts +/-%.1f per tick", reg, mag)
    while True:
        await asyncio.sleep(2)
        for reg, mag in specs:
            try:
                [val] = store.getValues(3, reg, 1)
                new_val = max(0, min(65535, int(val + random.uniform(-mag, mag))))
                store.setValues(3, reg, [new_val])
                store.setValues(4, reg, [new_val])
            except Exception as exc:
                log.warning("Simulation error (HR%d): %s", reg, exc)


async def simulate_sensor_waveform(store: ModbusSlaveContext) -> None:
    """
    Background task for DEVICE_CATEGORY=smart-sensor: writes the configured
    waveform (plus noise) to SENSOR_MODBUS_REGISTER every SENSOR_SAMPLE_RATE_MS.
    Replaces simulate()/DRIFT for this category -- see _sensor_value_at().
    """
    if CATEGORY != "smart-sensor":
        return
    log.info(
        "Smart-sensor simulation: kind=%s waveform=%s range=[%.2f, %.2f] "
        "noise=%.1f%% register=HR%d rate=%dms",
        SENSOR_KIND, SENSOR_WAVEFORM, SENSOR_MIN_VALUE, SENSOR_MAX_VALUE,
        SENSOR_NOISE_PERCENT, SENSOR_MODBUS_REGISTER, SENSOR_SAMPLE_RATE_MS,
    )
    t = 0.0
    dt = SENSOR_SAMPLE_RATE_MS / 1000.0
    full_range = SENSOR_MAX_VALUE - SENSOR_MIN_VALUE
    while True:
        await asyncio.sleep(dt)
        t += dt
        try:
            value = _sensor_value_at(t)
            if SENSOR_NOISE_PERCENT > 0:
                value += random.gauss(0.0, full_range * SENSOR_NOISE_PERCENT / 100.0)
            value = max(SENSOR_MIN_VALUE, min(SENSOR_MAX_VALUE, value))
            reg_val = max(0, min(65535, round(value * 10)))
            store.setValues(3, SENSOR_MODBUS_REGISTER, [reg_val])
            store.setValues(4, SENSOR_MODBUS_REGISTER, [reg_val])
        except Exception as exc:
            log.warning("Smart-sensor simulation error: %s", exc)


async def simulate_controller_reactive(store: ModbusSlaveContext) -> None:
    """
    Background task for DEVICE_CATEGORY=smart-controller: makes CO0 (the command
    coil a student/FUXA toggle writes) actually do something, instead of being a
    writable-but-inert register. Ticks every 1 s:

      pump/vfd/actuator/valve -- DI0 mirrors CO0 (status feedback, separate from the
        command coil like a real device). HR0 is the kind's headline live value when
        CO0=1 (running/open), 0 when CO0=0 (stopped/closed):
          pump  -> CONTROLLER_RATED_FLOW_LPM (with mild noise)
          vfd   -> CONTROLLER_MAX_FREQUENCY_HZ
          actuator/valve -> 100.0% (open/extended) -- simple position status, no
                            separate rated value exists for these kinds

      wellhead-controller -- no coil; HR0/HR1 are direct read/write setpoints
        (CONTROLLER_CHOKE_POSITION_PCT / CONTROLLER_DOWNHOLE_PRESSURE_SETPOINT_BAR),
        already seeded in build_store(). This loop intentionally does nothing for
        this kind -- whatever a student last wrote to HR0/HR1 stands; there's no
        coupled physics between choke position and downhole pressure in this phase.

    All values are x10 fixed-point, same convention as the rest of this file.
    """
    if CATEGORY != "smart-controller" or CONTROLLER_KIND == "wellhead-controller":
        return
    log.info(
        "Smart-controller simulation: kind=%s rated_flow=%.1f max_freq=%.1f "
        "(CO0=command, DI0=status, HR0=live value)",
        CONTROLLER_KIND, CONTROLLER_RATED_FLOW_LPM, CONTROLLER_MAX_FREQUENCY_HZ,
    )
    while True:
        await asyncio.sleep(1.0)
        try:
            [running] = store.getValues(1, 0, 1)  # FC1: read CO0
            store.setValues(2, 0, [1 if running else 0])  # FC2 datastore: DI0 mirrors CO0

            if not running:
                value = 0.0
            elif CONTROLLER_KIND == "pump":
                value = CONTROLLER_RATED_FLOW_LPM + random.gauss(0.0, CONTROLLER_RATED_FLOW_LPM * 0.02)
            elif CONTROLLER_KIND == "vfd":
                value = CONTROLLER_MAX_FREQUENCY_HZ
            else:  # actuator, valve -- simple open/extended position status
                value = 100.0

            reg_val = max(0, min(65535, round(max(0.0, value) * 10)))
            store.setValues(3, 0, [reg_val])
            store.setValues(4, 0, [reg_val])
        except Exception as exc:
            log.warning("Smart-controller simulation error: %s", exc)


