# Main entry point
# ══════════════════════════════════════════════════════════════════════════════════

async def main() -> None:
    """
    Starts both servers and the simulation task in a single asyncio event loop.

    Startup order:
      1. Build the Modbus datastore and set the module-level _store reference.
      2. Start the HTTP configuration portal on port 80 (background task).
      3. Start the register drift simulation (background task).
      4. Start the Modbus TCP server on port 502 (blocks until shutdown).
    """
    global _store

    log.info(
        "Device=%s  label=%s  category=%s  unit=%d  modbus=%d  web=%d",
        DEVICE_ID, DEVICE_LABEL, CATEGORY, UNIT_ID, PORT, WEB_PORT,
    )

    _store = build_store()

    # HTTP configuration portal
    try:
        web = await asyncio.start_server(http_handler, "0.0.0.0", WEB_PORT)
        asyncio.ensure_future(web.serve_forever())
        log.info("HTTP portal: http://0.0.0.0:%d/ (login: %s / %s)", WEB_PORT, WEB_USER, WEB_PASS)
    except OSError as exc:
        log.warning("Could not bind HTTP portal on port %d: %s", WEB_PORT, exc)

    # Register drift simulation (or waveform/reactive simulation for smart-sensor/-controller)
    asyncio.ensure_future(simulate(_store))
    asyncio.ensure_future(simulate_sensor_waveform(_store))
    asyncio.ensure_future(simulate_controller_reactive(_store))

    # Modbus TCP server (blocks)
    context = ModbusServerContext(slaves={UNIT_ID: _store}, single=False)
    await StartAsyncTcpServer(context, address=("0.0.0.0", PORT))


if __name__ == "__main__":
    asyncio.run(main())
